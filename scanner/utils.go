func WorkerPool(jobs []string, workers int, fn func(string) bool) []string {
	var results []string
	var mu sync.Mutex
	var wg sync.WaitGroup
	sem := make(chan struct{}, workers)

	for _, job := range jobs {
		wg.Add(1)
		sem <- struct{}{}
		go func(j string) {
			defer wg.Done()
			defer func() { <-sem }()
			if fn(j) {
				mu.Lock()
				results = append(results, j)
				mu.Unlock()
			}
		}(job)
	}
	wg.Wait()
	return results
}

